"use client";

import { forwardRef, type InputHTMLAttributes } from "react";

interface InputProps extends InputHTMLAttributes<HTMLInputElement> {
  label?: string;
  error?: string;
  hint?: string;
}

export const Input = forwardRef<HTMLInputElement, InputProps>(
  ({ label, error, hint, className = "", id, ...props }, ref) => {
    const inputId = id ?? label?.toLowerCase().replace(/\s+/g, "-");

    return (
      <div className="w-full">
        {label && (
          <label
            htmlFor={inputId}
            className="block text-sm font-medium text-ink mb-1.5"
          >
            {label}
          </label>
        )}
        <input
          ref={ref}
          id={inputId}
          className={`
            w-full px-4 py-3 rounded border bg-bone
            text-ink placeholder:text-ink-faint
            transition-all duration-200
            focus:outline-none focus:ring-2 focus:ring-coral/30 focus:border-coral
            disabled:bg-paper-dark disabled:opacity-60 disabled:cursor-not-allowed
            ${error ? "border-coral/50 ring-2 ring-coral/20" : "border-paper-dark hover:border-ink-mute"}
            ${className}
          `}
          aria-invalid={!!error}
          aria-describedby={error ? `${inputId}-error` : hint ? `${inputId}-hint` : undefined}
          {...props}
        />
        {error && (
          <p id={`${inputId}-error`} className="mt-1.5 text-sm text-coral">
            {error}
          </p>
        )}
        {hint && !error && (
          <p id={`${inputId}-hint`} className="mt-1.5 text-sm text-ink-faint">
            {hint}
          </p>
        )}
      </div>
    );
  }
);

Input.displayName = "Input";
